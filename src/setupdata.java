    private void applyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyActionPerformed
        // TODO add your handling code here:
        String electricity = elec.getText();
        String internet = inte.getText();
        String business = busi.getText();
        String staff = staf.getText();
        String rent = room.getText();
        String user = account.getText();
        if (electricity != null && internet != null && business != null && staff != null && rent != null){
            try{
                conn = DriverManager.getConnection("jdbc:derby://localhost:1527/bactong","bactong","bactong");
                st = (Statement)conn.createStatement();
                st.executeUpdate("UPDATE BACTONG.EXP SET ELECTRICITY = "+electricity+", INTERNET = "+internet+", BUSINESSTAX = "+business+", STAFFPAYMENT = "+staff+", ROOMRENTPAYMENT = "+rent+" WHERE USERNAME = '"+user+"'");
                final JPanel panel = new JPanel();
                JOptionPane.showMessageDialog(panel,"Shop Expenses Registered","Shop Expenses Notice", JOptionPane.INFORMATION_MESSAGE);
                String msg = account.getText();
                dispose();
                home info = new home(msg);
                info.setVisible(true);
            }
            catch (Exception ex){
                Logger.getLogger(loginbactong.class.getName()).log(Level.SEVERE,null,ex);
                final JPanel panel = new JPanel();
                JOptionPane.showMessageDialog(panel,"Please fill out all the box with numbers,\nInput 0 if none","Shop Expenses Notice", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_applyActionPerformed

